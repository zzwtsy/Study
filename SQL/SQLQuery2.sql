--提取身份证号中的出生日期
DECLARE @ID CHAR(18)
SET @ID = '340202200004106891'
SELECT SUBSTRING(@ID,7,4)+'-'+SUBSTRING(@ID,11,2)+'-'+SUBSTRING(@ID,13,2)

--返回姓名姓氏
DECLARE @NAME VARCHAR(3)
SET @NAME = '孟繁兴'
SELECT LEFT(@NAME,1)

--隐藏手机号4-7位
DECLARE @NUMBER CHAR(11)
SET @NUMBER = '13305643727'
SELECT STUFF(@NUMBER,4,4,'****')

--100 天后是那天
SELECT DATEPART(DAY,DATEADD(DAY,100,GETDATE()))

--还有多少天过年
DECLARE @Y CHAR(8)
SET @Y = CONVERT(CHAR(4),(DATEPART(YEAR, GETDATE())+1),110) + '-1-1'
SELECT DATEDIFF(DAY,GETDATE(), @Y )

--获取1~10之间的随机数
SELECT CAST((RAND()*10+1) AS INT)

--提取身份证号中的出生日期并计算年龄
DECLARE @ID2 CHAR(18)
SET @ID2 = '340202200004106891'
SELECT DATEDIFF(YEAR,SUBSTRING(@ID2,7,4),GETDATE())
